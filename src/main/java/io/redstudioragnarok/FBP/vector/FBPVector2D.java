package io.redstudioragnarok.FBP.vector;

public class FBPVector2D {

    /** The x component of this vector. */
    public float x;
    /** The y component of this vector. */
    public float y;

    public FBPVector2D() {
    }

    public FBPVector2D(float inputX, float inputY) {
        this.x = inputX;
        this.y = inputY;
    }
}
